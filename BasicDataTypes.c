#include <stdio.h>
int main()
{
    //Definition and initializing d and f.
    int a = 3, b = 5, c;
    c = a + b;
    printf("Value of c: %d \n", c);

    //The variable x has the value 'E'.
    char x = 'E';
    char y = 70;

    //ASCII Code.
    printf("x = %c, x = %d\n", x, x);
    printf("y = %c, y = %d\n", y, y);

    char z = y + a;
    printf("z = %c, z = %d\n", z, z);
    
    return 0;
}